create or replace function public.apply_lab_result_to_batch()
returns trigger
language plpgsql
as $$
begin
  if new.stage = 'pre_pasteurization' and new.result = 'pending' then
    update public.batches set status = 'pre_testing' where id = new.batch_id and status = 'raw';
  elsif new.stage = 'pre_pasteurization' and new.result = 'passed' then
    update public.batches set status = 'pre_test_passed' where id = new.batch_id;
  elsif new.stage = 'pre_pasteurization' and new.result = 'failed' then
    update public.batches set status = 'discarded', discarded_reason = 'failed pre-pasteurization lab test' where id = new.batch_id;
  elsif new.stage = 'post_pasteurization' and new.result = 'pending' then
    update public.batches set status = 'post_testing' where id = new.batch_id and status = 'pasteurized';
  elsif new.stage = 'post_pasteurization' and new.result = 'passed' then
    update public.batches set status = 'ready' where id = new.batch_id;
  elsif new.stage = 'post_pasteurization' and new.result = 'failed' then
    update public.batches set status = 'discarded', discarded_reason = 'failed post-pasteurization lab test' where id = new.batch_id;
  end if;

  return new;
end;
$$;

create trigger lab_results_apply_batch_status
after insert or update of stage, result on public.lab_results
for each row execute function public.apply_lab_result_to_batch();
