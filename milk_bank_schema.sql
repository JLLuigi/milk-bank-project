create or replace function public.refresh_batch_total_volume()
returns trigger
language plpgsql
as $$
declare
  target_batch_id uuid;
begin
  if tg_op = 'DELETE' then
    target_batch_id := old.batch_id;
  else
    target_batch_id := new.batch_id;
  end if;

  update public.batches b
  set total_volume_ml = coalesce((
    select sum(volume_ml)
    from public.batch_collections bc
    where bc.batch_id = target_batch_id
  ), 0)
  where b.id = target_batch_id;

  if tg_op = 'UPDATE' and old.batch_id is distinct from new.batch_id then
    update public.batches b
    set total_volume_ml = coalesce((
      select sum(volume_ml)
      from public.batch_collections bc
      where bc.batch_id = old.batch_id
    ), 0)
    where b.id = old.batch_id;
  end if;

  if tg_op = 'DELETE' then
    return old;
  end if;

  return new;
end;
$$;

create trigger batch_collections_refresh_batch_total
after insert or update or delete on public.batch_collections
for each row execute function public.refresh_batch_total_volume();
