-- Makati Human Milk Bank Management System
-- Canonical Supabase/PostgreSQL schema.
--
-- This file is the data-handling source of truth for backend implementation.
-- Keep application types, tests, RLS policies, and server actions aligned to it.
--
-- Current project decisions applied:
-- - Supabase Auth owns credentials; public.profiles owns app role/profile data.
-- - App roles are admin and staff.
-- - Beneficiary notifications are email/FIFO, not SMS.
-- - Inventory is derived from batch/bottle state. Do not create editable inventory rows.

