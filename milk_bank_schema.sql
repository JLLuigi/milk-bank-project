returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    if new.expected_result_at is null then
      new.expected_result_at = new.sent_to_lab_at + interval '14 days';
    end if;
  elsif new.expected_result_at is null
        or new.sent_to_lab_at is distinct from old.sent_to_lab_at then
    new.expected_result_at = new.sent_to_lab_at + interval '14 days';
  end if;

  return new;
end;
$$;

