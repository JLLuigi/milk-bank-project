create or replace function public.prevent_invalid_batch_status_transition()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    return new;
  end if;

  if new.status = old.status then
    return new;
  end if;

  if old.status = 'raw' and new.status in ('pre_testing', 'discarded') then
    return new;
  elsif old.status = 'pre_testing' and new.status in ('pre_test_passed', 'discarded') then
    return new;
  elsif old.status = 'pre_test_passed' and new.status in ('pasteurized', 'discarded') then
    return new;
  elsif old.status = 'pasteurized' and new.status in ('post_testing', 'discarded') then
    return new;
  elsif old.status = 'post_testing' and new.status in ('ready', 'discarded') then
    return new;
  elsif old.status = 'ready' and new.status in ('dispensed', 'discarded') then
    return new;
  elsif old.status in ('dispensed', 'discarded') then
    raise exception 'batch status % is terminal and cannot transition to %', old.status, new.status;
  end if;

  raise exception 'invalid batch status transition from % to %', old.status, new.status;
end;
$$;

create trigger batches_prevent_invalid_status_transition
before update of status on public.batches
for each row execute function public.prevent_invalid_batch_status_transition();

create or replace function public.prevent_collection_daily_limit_excess()
returns trigger
language plpgsql
as $$
declare
  day_total integer;
begin
  select coalesce(sum(volume_ml), 0)
  into day_total
  from public.collections
  where donor_id = new.donor_id
    and collected_at::date = new.collected_at::date
    and id <> coalesce(new.id, '00000000-0000-0000-0000-000000000000'::uuid);

  if day_total + new.volume_ml > 800 then
    raise exception 'daily collection limit exceeded: donor % would reach % mL', new.donor_id, day_total + new.volume_ml;
  end if;

  return new;
end;
$$;

create trigger collections_prevent_daily_limit_excess
before insert or update of donor_id, collected_at, volume_ml on public.collections
for each row execute function public.prevent_collection_daily_limit_excess();

