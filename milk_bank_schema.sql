create view public.inventory_summary
with (security_invoker = true) as
select
  b.status as batch_status,
  b.program,
  count(distinct b.id) as batch_count,
  count(bo.id) as bottle_count,
  coalesce(sum(bo.remaining_volume_ml), 0) as remaining_volume_ml,
  min(bo.expires_at) filter (where bo.status = 'available') as nearest_expiry
from public.batches b
left join public.bottles bo on bo.batch_id = b.id
group by b.status, b.program;

create view public.available_bottles_fifo
with (security_invoker = true) as
select
  bo.id as bottle_id,
  bo.bottle_number,
  bo.batch_id,
  b.batch_number,
  b.program,
  bo.remaining_volume_ml,
  bo.expires_at,
  bo.created_at
from public.bottles bo
join public.batches b on b.id = bo.batch_id
where b.status = 'ready'
  and bo.status = 'available'
  and bo.remaining_volume_ml > 0
order by bo.expires_at nulls last, bo.created_at;

create view public.waitlist_fifo
with (security_invoker = true) as
select
  i.id as inquiry_id,
  i.beneficiary_id,
  be.guardian_name,
  be.baby_name,
  be.contact_email,
  i.requested_volume_ml,
  i.requested_at
from public.inquiries i
join public.beneficiaries be on be.id = i.beneficiary_id
where i.status = 'waiting'
  and i.nicu_confirmed = true
order by i.requested_at;

create view public.collection_unit_report_base
with (security_invoker = true) as
select
  c.program,
  date_trunc('month', c.collected_at)::date as report_month,
  count(*) as collection_count,
  sum(c.volume_ml) as raw_collected_ml,
  coalesce(sum(bc.volume_ml) filter (where b.status = 'discarded'), 0) as discarded_ml,
  coalesce(sum(bc.volume_ml) filter (where b.status in ('ready', 'dispensed')), 0) as cleared_for_use_ml
from public.collections c
left join public.batch_collections bc on bc.collection_id = c.id
left join public.batches b on b.id = bc.batch_id
group by c.program, date_trunc('month', c.collected_at)::date;

