create or replace function public.apply_pasteurization_to_batch()
returns trigger
language plpgsql
as $$
begin
  if not exists (
    select 1 from public.batches
    where id = new.batch_id and status = 'pre_test_passed'
  ) then
    raise exception 'batch % must be pre_test_passed before pasteurization', new.batch_id;
  end if;

  update public.batches
  set status = 'pasteurized'
  where id = new.batch_id;

  return new;
end;
$$;

